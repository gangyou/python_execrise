class movement(object):
    def __init__(self, x_delta, y_delta, direct):
        self._x_delta = x_delta
        self._y_delta = y_delta
        self._direct = direct

    @property
    def deltaX(self): return self._x_delta

    @property
    def deltaY(self): return self._y_delta

    @property
    def toDirect(self): return self._direct
        
class step(object):
    def __init__(self, x, y, direct=''):
        self._x, self._y, self._direct = x, y, direct

    @property
    def x(self): return self._x

    @x.setter
    def x(self, value): self._x = value

    @property
    def y(self): return self._y

    @y.setter
    def y(self, value): self._y = value

    @property
    def direct(self): return self._direct

    @direct.setter
    def direct(self, value): self._direct = value

    def __repr__(self): return "x: {}, y: {}, to: {}".format(self.x, self.y, self.direct)
        
class stack(object):
    def __init__(self):
        self.data = []

    def push(self, data):
        self.data.append(data)

    def pop(self): return self.data.pop()

    def __iter__(self): return iter(self.data)

    def empty(self): return len(self.data) == 0
        
MOVEMENT = [
    movement(1, 0, 'S'),
    movement(-1, 0, 'N'),
    movement(0, 1, 'E'),
    movement(0, -1, 'W'),
]

def checkio(data):
    # 从(1,1)出发
    s = stack()
    pos = step(1, 1)

    while (pos.x, pos.y) != (10, 10):
        for m in MOVEMENT:
            x = pos.x + m.deltaX
            y = pos.y + m.deltaY
            d = m.toDirect
            if data[x][y] == 0:
                # push current step in stack
                pos.direct = d
                s.push(pos)
                # move to new position
                pos = step(x, y)
                # mark as visited
                data[x][y] = -1
                break
        else:
            pos = s.pop()

        if s.empty(): return ''

    return "".join([move.direct for move in s])

